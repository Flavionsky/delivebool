    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {

        $gateway = new Braintree\Gateway([
            'environment' => config('services.braintree.environment'),
            'merchantId' => config('services.braintree.merchantId'),
            'publicKey' => config('services.braintree.publicKey'),
            'privateKey' => config('services.braintree.privateKey')
        ]);

        $amount = $request->amount;
        $nonce = $request->payment_method_nonce;

        $result = $gateway->transaction()->sale([
            'amount' => $amount,
            'paymentMethodNonce' => $nonce,
            'options' => [
                'submitForSettlement' => true
            ]
        ]);

        if ($result->success) {
            $transaction = $result->transaction;

            $data = $request->all();

            $food = $data['itemid'];

            $order = new Order;

            $order->email = $data['email'];
            $order->name = $data['name_on_card'];
            $order->surname = $data['surname_on_card'];
            $order->city = $data['city'];
            $order->mobile_phone = $data['mobile_phone'];
            $order->total_price = $data['amount'];

            $order->foods()->sync($food);

            $order->save();

            return back()->with('success_message', 'Pagamento completato.Il tuo ID ordine Ã¨:' . $transaction->id);
        } else {
            $errorString = "";

            foreach ($result->errors->deepAll() as $error) {
                $errorString .= 'Error: ' . $error->code . ": " . $error->message . "\n";
            }

            return back()->withErrors('An error occurred with the message: ' . $result->message);
        }
    }

